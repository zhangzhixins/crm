package com.hy.crm.pojo.vo;

public class BusFunnel {
    private Integer count;
    private Integer money;
    private String  baiFenShu;
    private String  baiFenJin;

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public Integer getMoney() {
        return money;
    }

    public void setMoney(Integer money) {
        this.money = money;
    }

    public String getBaiFenShu() {
        return baiFenShu;
    }

    public void setBaiFenShu(String baiFenShu) {
        this.baiFenShu = baiFenShu;
    }

    public String getBaiFenJin() {
        return baiFenJin;
    }

    public void setBaiFenJin(String baiFenJin) {
        this.baiFenJin = baiFenJin;
    }
}
